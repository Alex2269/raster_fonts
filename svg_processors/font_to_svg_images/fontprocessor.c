// Глобальні змінні для бібліотеки FreeType та шрифту
// Ініціалізуються один раз в main() для економії ресурсів
static FT_Library library = NULL;
static FT_Face font_face = NULL;
static int debug_file = -1;

// Буфери для формування рядків (зроблені статичними для безпеки)
static char mesg[256];   // Збільшено розмір для float координат
static char buffer[256];

// Функції
int generate(int num_code_symbol);
uint32_t lv_rand(uint32_t min, uint32_t max);
void write_svg(const char *line, int flag);
void error_print(const char *msg, int error);

// Структура користувача для передачі даних в callback-и
typedef struct {
    int state;
} User;

// ============================================================================
// CALLBACK-ФУНКЦІЇ ДЛЯ ОБРОБКИ КОНТУРІВ ГЛІФІВ
// FreeType повертає координати у форматі 26.6 (fixed-point)
// Ділимо на 64.0 для отримання float координат з високою точністю
// ============================================================================

/**
 * Callback для команди "MoveTo" - переміщення початку нового контуру
 */
int MoveToFunction(const FT_Vector* to, void* user) {
    // Ділимо на 64.0 для конвертації з формату 26.6 у звичайні координати
    // %.2f дає 2 знаки після коми для гладких кривих
    snprintf(mesg, sizeof(mesg), "M %.2f %.2f\n", to->x / 64.0, to->y / 64.0);
    write_svg(mesg, 0);
    return 0;
}

/**
 * Callback для команди "LineTo" - малювання прямої лінії
 */
int LineToFunction(const FT_Vector *to, void* user) {
    snprintf(mesg, sizeof(mesg), "L %.2f %.2f\n", to->x / 64.0, to->y / 64.0);
    write_svg(mesg, 0);
    return 0;
}

/**
 * Callback для команди "ConicTo" - квадратична крива Безьє
 * Використовується в більшості шрифтів (TrueType)
 */
int ConicToFunction(const FT_Vector *control, const FT_Vector *to, void* user) {
    snprintf(mesg, sizeof(mesg), "Q %.2f %.2f, %.2f %.2f\n", 
             control->x / 64.0, control->y / 64.0,  // Контрольна точка
             to->x / 64.0, to->y / 64.0);            // Кінцева точка
    write_svg(mesg, 0);
    return 0;
}

/**
 * Callback для команди "CubicTo" - кубічна крива Безьє
 * Використовується в PostScript шрифтах
 */
int CubicToFunction(const FT_Vector *c1, const FT_Vector *c2, const FT_Vector *to, void* user) {
    snprintf(mesg, sizeof(mesg), "C %.2f %.2f %.2f %.2f %.2f %.2f\n",
             c1->x / 64.0, c1->y / 64.0,   // Перша контрольна точка
             c2->x / 64.0, c2->y / 64.0,   // Друга контрольна точка
             to->x / 64.0, to->y / 64.0);   // Кінцева точка
    write_svg(mesg, 0);
    return 0;
}

// ============================================================================
// ДОПОМІЖНІ ФУНКЦІЇ
// ============================================================================

/**
 * Запис рядка в SVG файл
 * @param line - рядок для запису
 * @param flag - якщо 1, записує SVG_START_STRING з хедера, інакше line
 */
void write_svg(const char *line, int flag) {
    if (flag) {
        write(debug_file, SVG_START_STRING, strlen(SVG_START_STRING));
    } else {
        write(debug_file, line, strlen(line));
    }
}

/**
 * Обробка помилок з виводом повідомлення та очищенням ресурсів
 */
void error_print(const char *msg, int error) {
    fprintf(stderr, "❌ Error code: %d | Message: %s\n", error, msg);
    
    // Звільняємо ресурси перед виходом (уникаємо витоків пам'яті)
    if (font_face) 
        FT_Done_Face(font_face);
    if (library) 
        FT_Done_FreeType(library);
    
    exit(error);
}

/**
 * Генератор псевдовипадкових чисел (алгоритм XorShift)
 * Використовується для генерації випадкових кольорів
 */
uint32_t lv_rand(uint32_t min, uint32_t max) {
    static uint32_t a = 0x1234ABCD;  // Початкове зерно (seed)
    
    // Алгоритм "xor" з статті Marsaglia "Xorshift RNGs"
    uint32_t x = a;
    x ^= x << 13;
    x ^= x >> 17;
    x ^= x << 5;
    a = x;
    
    return (a % (max - min + 1)) + min;
}

// ============================================================================
// ГОЛОВНА ФУНКЦІЯ
// ============================================================================

int main(int argc, char *argv[]) {
    int error;
    
    printf("🚀 Початок генерації SVG іконок...\n");
    
    // ========================================================================
    // КРОК 1: Створення директорії (один раз перед циклом)
    // ========================================================================
    if (mkdir("dest_dir", S_IRWXU) == -1) {
        // Ігноруємо помилку, якщо директорія вже існує
        if (errno != EEXIST) {
            perror("❌ Не вдалося створити директорію dest_dir");
            return 1;
        }
    }
    printf("📁 Директорія dest_dir готова\n");
    
    // ========================================================================
    // КРОК 2: Ініціалізація бібліотеки FreeType (ОДИН РАЗ!)
    // ========================================================================
    if ((error = FT_Init_FreeType(&library))) {
        error_print("Ініціалізація бібліотеки FreeType не вдалася", error);
    }
    printf("✅ FreeType ініціалізовано\n");
    
    // ========================================================================
    // КРОК 3: Завантаження шрифту (ОДИН РАЗ!)
    // ========================================================================
    if ((error = FT_New_Face(library, PATH_AWESOME, 0, &font_face))) {
        error_print("Не вдалося завантажити шрифт", error);
    }
    printf("📖 Шрифт завантажено: %s\n", PATH_AWESOME);
    
    // ========================================================================
    // КРОК 4: Налаштування розміру шрифту (ОДИН РАЗ!)
    // ========================================================================
    // 0 - автоматична ширина, 16 - висота в пікселях
    if ((error = FT_Set_Pixel_Sizes(font_face, 0, 16))) {
        error_print("Не вдалося встановити розмір шрифту", error);
    }
    printf("📏 Розмір шрифту: 16 пікселів\n");
    
    // ========================================================================
    // КРОК 5: Масив кодів символів для генерації
    // ========================================================================
    int codes[] = {
        61441, 61448, 61451, 61452, 61452, 61453, 61457, 61459, 61461, 61465,
        61468, 61473, 61478, 61479, 61480, 61502, 61512, 61515, 61516, 61517,
        61521, 61522, 61523, 61524, 61543, 61544, 61550, 61552, 61553, 61556,
        61559, 61560, 61561, 61563, 61587, 61589, 61636, 61637, 61639, 61671,
        61674, 61683, 61724, 61732, 61787, 61931, 62016, 62017, 62018, 62019,
        62020, 62087, 62099, 62189, 62212, 62810, 63426, 63650, 63133
    };
    int num_codes = sizeof(codes) / sizeof(codes[0]);
    
    // ========================================================================
    // КРОК 6: Генерація цифр (0-9)
    // ========================================================================
    printf("\n🔢 Генерація цифр 0-9...\n");
    for (int i = 48; i < 58; i++) {
        generate(i);
    }
    
    // ========================================================================
    // КРОК 7: Генерація іконок з масиву
    // ========================================================================
    printf("🎨 Генерація іконок...\n");
    for (int i = 0; i < num_codes; i++) {
        generate(codes[i]);
        if ((i + 1) % 10 == 0) {
            printf("   Оброблено: %d/%d\n", i + 1, num_codes);
        }
    }
    
    // ========================================================================
    // КРОК 8: Звільнення ресурсів
    // ========================================================================
    FT_Done_Face(font_face);
    FT_Done_FreeType(library);
    
    printf("\n✅ Генерацію завершено! Файли збережено в папці dest_dir/\n");
    return 0;
}

// ============================================================================
// ФУНКЦІЯ ГЕНЕРАЦІЇ ОДНОГО СИМВОЛУ
// ============================================================================

/**
 * Генерація SVG файлу для одного гліфа (символу)
 * @param num_code_symbol - Unicode код символу
 * @return 0 при успіху, -1 при помилці
 */
int generate(int num_code_symbol) {
    FT_GlyphSlot slot = font_face->glyph;
    FT_Outline outline;
    FT_Outline_Funcs outlinefuncs;
    FT_Matrix matrix;
    FT_BBox boundingBox;
    User user = {0};
    int error;
    
    // ========================================================================
    // КРОК 1: Завантаження гліфа в слот
    // ========================================================================
    if ((error = FT_Load_Char(font_face, num_code_symbol, FT_LOAD_DEFAULT)) != 0) {
        fprintf(stderr, "⚠️  Гліф %d не знайдено, пропускаємо\n", num_code_symbol);
        return -1;  // Не завершуємо програму, просто пропускаємо
    }
    
    // ========================================================================
    // КРОК 2: Налаштування матриці трансформації
    // ========================================================================
    // Віддзеркалюємо по Y (бо в SVG вісь Y спрямована вниз, а в FreeType - вгору)
    // FreeType використовує формат 16.16 для матриць (множник 65536)
    matrix.xx =  1L * 65536L;   // Масштаб X = 1.0
    matrix.xy =  0L;             // Без нахилу
    matrix.yx =  0L;             // Без нахилу
    matrix.yy = -1L * 65536L;   // Масштаб Y = -1.0 (віддзеркалення)
    
    // ========================================================================
    // КРОК 3: Отримання контуру гліфа
    // ========================================================================
    outline = slot->outline;
    
    // Налаштування callback-функцій для розбору контуру
    outlinefuncs.shift    = 0;
    outlinefuncs.delta    = 0;
    outlinefuncs.move_to  = MoveToFunction;
    outlinefuncs.line_to  = LineToFunction;
    outlinefuncs.conic_to = ConicToFunction;
    outlinefuncs.cubic_to = CubicToFunction;
    
    // ========================================================================
    // КРОК 4: Трансформація та отримання обмежувальної рамки (bounding box)
    // ========================================================================
    FT_Outline_Transform(&outline, &matrix);
    FT_Outline_Get_BBox(&outline, &boundingBox);
    
    // Конвертація координат з формату 26.6 у float
    // Ділимо на 64.0 для отримання нормальних піксельних значень
    double xMin = boundingBox.xMin / 64.0;
    double yMin = boundingBox.yMin / 64.0;
    double xMax = boundingBox.xMax / 64.0;
    double yMax = boundingBox.yMax / 64.0;
    
    double width  = xMax - xMin;
    double height = yMax - yMin;
    
    // ========================================================================
    // КРОК 5: Відкриття файлу для запису
    // ========================================================================
    snprintf(buffer, sizeof(buffer), "./dest_dir/%d.svg", num_code_symbol);
    
    // O_CREAT - створити якщо не існує
    // O_WRONLY - запис тільки для читання
    // O_TRUNC - очистити файл якщо існує
    debug_file = open(buffer, O_CREAT | O_WRONLY | O_TRUNC, 0666);
    
    if (debug_file == -1) {
        perror("❌ Не вдалося відкрити файл");
        return -1;
    }
    
    // ========================================================================
    // КРОК 6: Запис SVG заголовка та viewBox
    // ========================================================================
    write_svg("", 1);  // Запис SVG_START_STRING з хедера
    
    // Формуємо viewBox з float координатами для точності
    snprintf(mesg, sizeof(mesg), 
             "viewBox='%.2f %.2f %.2f %.2f'>\n<path d='\n", 
             xMin, yMin, width, height);
    write_svg(mesg, 0);
    
    // ========================================================================
    // КРОК 7: Розбір контуру та запис шляхів (path data)
    // ========================================================================
    FT_Outline_Decompose(&outline, &outlinefuncs, &user);
    
    // ========================================================================
    // КРОК 8: Запис стилів (ТІЛЬКИ заповнення, без обводки!)
    // ========================================================================
    // Важливо: fill='none' прибрано, stroke='black' прибрано
    // Залишено тільки заповнення випадковим кольором
    write_svg("\n'\n style='fill:#", 0);
    
    // Генерація випадкового кольору в hex форматі (6 символів)
    uint32_t color = lv_rand(0x8080ff, 0xffffff) & 0xFFFFFF;
    snprintf(buffer, sizeof(buffer), "%06x", color);
    write_svg(buffer, 0);
    
    // Завершення тега path та SVG
    write_svg(";' fill-rule='evenodd'/>\n</svg>\n", 0);
    
    // ========================================================================
    // КРОК 9: Закриття файлу (важливо!)
    // ========================================================================
    close(debug_file);
    
    return 0;
}
